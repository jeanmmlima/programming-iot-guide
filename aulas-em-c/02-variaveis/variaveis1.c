#include <stdio.h>
int main()
{
   //declarando variáveis: tipo e nome
    int quantidade;
    char qualidade;
    float peso;

    //atribuindo valor a variáveis
    quantidade = 3;
    qualidade = 'A';
    peso = 0.653;

    //exibindo o valor das variáveis
    printf("%i \n",quantidade);
    printf("%c \n", qualidade);
    printf("%f \n", peso);
    return 0;
}
