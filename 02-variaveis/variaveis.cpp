#include <iostream>
using namespace std;

int main(void){

    //declarando variáveis: tipo e nome
    int quantidade;
    char qualidade;
    float peso;

    //atribuindo valor a variáveis
    quantidade = 3;
    qualidade = 'A';
    peso = 0.653;

    //exibindo o valor das variáveis
    cout << quantidade << endl;
    cout << qualidade << "\n";
    cout << peso << endl;

    return 0;

}